
#include <stdio.h>

int main(void){

  int a[4] = {1,2,3,4};
  printf("  a  = %d\n",a);
  printf(" &a  = %d\n",&a);
  printf("a+1  = %d\n",a + 1);
  printf("&a+1 = %d\n",&a + 1);

  return 0;
}


//ps：&（取地址）的优先级要高于+（二元加）
//从打印出来的地址我们可以看出，&a的地址与a是一样的，a+1指向了数组的第二个元素，而&a+1却跳过了整个数组的所有元素个数，指向数组之后的位置

//解释：a是一个指向int变量的指针，在这里指向了a[0]，而&a是一个指向一个包含了4个int值的数组的指针，所以这里a有隐式指针转换，而&a没有
